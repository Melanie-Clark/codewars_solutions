"""
Your task is to write a function maskify, which changes all but the last four characters into '#'.

Examples (input --> output):
"4556364607935616" --> "############5616"
     "64607935616" -->      "#######5616"
               "1" -->                "1"
                "" -->                 ""
"""
def maskify(cc):
    return ((len(cc) - 4) * "#") + cc[-4:]